package lt.bit.hw;

/**
 * LT Parašykite programą, kuri išveda į ekraną anglišką abėcėlę.
 * EN Write a program that outputs the English alphabet on the screen.
 */

public class OP_018 {
    public static void main(String[] args) {
        for (char i = 'A'; i <= 'Z'; i++) {
            System.out.println(i);
        }
    }
}
