package lt.bit.hw.calculatorOP_024;

public class ArithmeticOperations {
    public double addition(double numberOne, double numberTwo) {
        double addition = numberOne + numberTwo;
        return addition;
    }

    public double subtraction(double numberOne, double numberTwo) {
        double subtraction = numberOne - numberTwo;
        return subtraction;
    }

    public double multiplication(double numberOne, double numberTwo) {
        double multiplication = numberOne * numberTwo;
        return multiplication;
    }

    public double division(double numberOne, double numberTwo) {
        double division = numberOne / numberTwo;
        return division;
    }

    public double modulus(double numberOne, double numberTwo) {
        double modulus = numberOne % numberTwo;
        return modulus;
    }
}
